        <div class="accordion">
          <div class="quest-section"> <a class="quest-title" href="#accordion-1">Do you Offer any guarantees? </a>
            <div id="accordion-1" class="quest-content">
              <p>Please review our Terms and Conditions for details. Our crystal clear process ensures client’s security, therefore before proceeding with any order, we make sure we have sent a legal binding contract and a non-disclosure agreement stating the services you are entitled to and the terms and conditions.</p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-2"> What if I don’t need every service in your comprehensive packages?</a>
            <div id="accordion-2" class="quest-content">
              <p>Feel free to pick and choose which services you want from us. We can often reduce the pricing if you want to drop any of the service options. We’re happy to customize our services to meet your needs. You can get in touch with any of our online consultants who will guide you on the suitable service you need and the customized quotation. </p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-3">What if I just want a feedback or consultation on my book?</a>
            <div id="accordion-3" class="quest-content">
              <p>Our editing and coaching services are perfectly suited for writers who are looking to improve their work. We can give input at whatever level of detail you’d like—feedback on the structure and concept of the book, critical review on the flow and transitions, or copyediting changes to the text itself. We can also address specific issues, as per your request, in our review of your work. If you recommend our editors will make the changes and improve the work for you. </p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-4">My book requires research – Can your service provide that?</a>
            <div id="accordion-4" class="quest-content">
              <p>Yes, our bookwriting packages can include research services. Whether your book requires basic Internet searches or rigorous scholarly research, we have writers who can conduct effective research and synthesize it into your book.</p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-5">How involved would I need to be in the process? </a>
            <div id="accordion-5" class="quest-content">
              <p>Your level of involvement is entirely up to you. You can opt to work closely with your bookmaster, or simply provide them with basic information and let them do the rest of the work. This is your book, which means that you have final say on the content and it’s your decision how and when you give your input and approval. We’ll work with you to customize the process so that it fits your schedule and preferred working style. Although in an autobiography/memoir it is highly recommended to work side by side with your bookmaster. </p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-6">Can I meet and discuss my book with the bookmaster face to face?</a>
            <div id="accordion-6" class="quest-content">
              <p>We do not entertain walk-in customers, we are an online business and the process is convenient with telephonic interviews and chat sessions. Meeting with the clients would affect the productivity of the team and delay the time.  However under uncertain situation we meet clients if the order amount exceeds £30,000, to meet the special needs of the customer.</p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-7">How long does Book Masters process normally take?</a>
            <div id="accordion-7" class="quest-content">
              <p>The duration of the bookwriting process is partly up to you—the author! Depending on your schedule and desired level of involvement, your bookmaster will develop material for you to review and approve on a weekly or biweekly basis.</p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-8">How are writers screened to make sure they qualify for this network?</a>
            <div id="accordion-8" class="quest-content">
              <p>All applicants are screened and tested. Their credentials are checked. Writers and developmental editors need a track record of published books that they’ve written and edited. Based on that we will assign the writer to the task at hand. </p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-9">Are your services 100% confidential?</a>
            <div id="accordion-9" class="quest-content">
              <p>Yes, of course! Our services are 100% confidential. We will never use any of the information you provide us for any other purpose, nor will we use the writing developed for your book ever again. We have a very comprehensive privacy policy posted on our website, and we are also happy to send you a non-disclosure agreement. If you are happy and with your consent, you can leave a testimonial and we will use it on the website. </p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-10">Why do I need to talk to a Consultant?</a>
            <div id="accordion-10" class="quest-content">
              <p>Our consultants are available to discuss all aspects of your project with you, including planning, preparation, concept generation, organization, costs, development, style, tone, writing, editing, formatting, publishing, and more! Our consulting process is flexible and customizable to your needs and preferences. Consultation sessions generally take place by phone or Chat.<br/><br/>Once you’re happy with the content of your book, we can help you determine which publishing options work best for you and make your published book a reality! </p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-11">Is Book Masters ethical? </a>
            <div id="accordion-11" class="quest-content">
              <p>Absolutely! The most important elements in writing a book are the ideas—and as the author and initiator of your book, the ideas are all yours. Even if you don’t consider yourself a writer or are too busy to develop a publishable book, your voice still deserves to be heard. The bookwriting process is simply a means to get your wisdom, methods, story, or ideas on to paper.</p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-12">Do I have to give the bookmaster any credit or co-authorship? </a>
            <div id="accordion-12" class="quest-content">
              <p>No! That is why it is called bookwriting—no one will know you had help with the writing process unless you choose to tell them. Our services are 100% confidential. The story we help you to tell will always be your story. You are 100% the author of your book. We do not retain any rights or ownership of your book whatsoever. Even if your book earns a million dollars, you will never owe us any credit or extra compensation unless you choose to do so. Be skeptical of other bookwriting services that may require such promises from you. While credit is appreciated, it is never required. </p>
            </div>
          </div>
          <div class="quest-section"> <a class="quest-title" href="#accordion-13">I had a great/bad experience with the writer I selected. Should I tell the network coordinator?</a>
            <div id="accordion-13" class="quest-content">
              <p>Absolutely (in either case). Any writer who has several legitimate negative feedback reports will be removed from the network. Note: An editor refusing to work without prepayment or to do free additional work (above and beyond the scope outlined in the agreement) are not legitimate complaints; an editor missing a contractual deadline or becoming unavailable is. Because of the ethical guidelines of this network and the collection of feedback from network clients, legitimate complaints have rarely occurred since the company was formed in 2014. Please let the network coordinator know about your experience with your editor(s).</p>
